package com.yangmiao.bis.model;

public class ProduceInfo {
	
	public String number;
	
	public String leixing;
	
	public String yuqishouyi;
	
	public String qigoujine;
	
	public String licaiqixian;
	
	public String tianshu;
	

	public String getTianshu() {
		return tianshu;
	}

	public void setTianshu(String tianshu) {
		this.tianshu = tianshu;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getLeixing() {
		return leixing;
	}

	public void setLeixing(String leixing) {
		this.leixing = leixing;
	}

	public String getYuqishouyi() {
		return yuqishouyi;
	}

	public void setYuqishouyi(String yuqishouyi) {
		this.yuqishouyi = yuqishouyi;
	}

	public String getQigoujine() {
		return qigoujine;
	}

	public void setQigoujine(String qigoujine) {
		this.qigoujine = qigoujine;
	}

	public String getLicaiqixian() {
		return licaiqixian;
	}

	public void setLicaiqixian(String licaiqixian) {
		this.licaiqixian = licaiqixian;
	}
	
	
	
}
